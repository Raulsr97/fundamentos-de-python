# Operadores Numericos
a = 10
b = 3
print('Suma:', a + b)
print('Resta:', a - b)
print('Multiplicacion:', a * b)
print('Potenciacion:', a ** b)
print('Division:', a / b)
print('Parte entera de la division:', a // b)

# Residuo de una division
print('Modulo:', a % b)

a += 2
print(a)

operacion_1 = 2 + 3 * 4
operacion_2 = (2 + 3) * 4
print(operacion_1)
print(operacion_2)

# Operadores booleanos
print(operacion_2 > operacion_1)
print(operacion_1 > operacion_2)
print(operacion_2 >= operacion_1)
print(operacion_1 >= operacion_2)